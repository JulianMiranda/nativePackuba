export const convertPeso = (key: string) => {
  switch (key) {
    case 'oneandhalfkgPrice':
      return '1.5 Kg';

    case 'twokgPrice':
      return '2 Kg';

    case 'threekgPrice':
      return '3 Kg';

    case 'fourkgPrice':
      return '4 Kg';

    case 'fivekgPrice':
      return '5 Kg';

    case 'sixkgPrice':
      return '6 Kg';

    case 'sevenkgPrice':
      return '7 Kg';

    case 'eightkgPrice':
      return '8 Kg';

    case 'ninekgPrice':
      return '9 Kg';
    case 'tenkgPrice':
      return '10 Kg';
    case 'elevenkgPrice':
      return '11 Kg';
    case 'twelvekgPrice':
      return '12 Kg';
    case 'thirteenkgPrice':
      return '13 Kg';
    case 'fourteenkgPrice':
      return '14 Kg';
    case 'fifteenkgPrice':
      return '15 Kg';
    case 'sixteenkgPrice':
      return '16 Kg';
    case 'seventeenkgPrice':
      return '17 Kg';
    case 'eighteenkgPrice':
      return '18Kg';
    case 'nineteenkgPrice':
      return '19 Kg';
    case 'twentykgPrice':
      return '20 Kg';

    default:
      return '';
  }
};
